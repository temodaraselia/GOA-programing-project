// alert()-ით გამოუტანეთ შეტყობინება: "წვდომა შეზღუდულია". სხვა შემთხვევაში (18 ან მეტი) გამოუტანეთ: "წვდომა დაშვებულია".//


let age = prompt("Enter Your Age:");

if (age < 18){
    alert("წვდომა შეზღუდულია")
} else {
    alert("წვდომა დაშვებულია")
}