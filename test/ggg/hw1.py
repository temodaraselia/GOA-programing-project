# numbers = [3, -7, 12, 0, -4, 9, 15, -2]
# ა) გამოიყენეთ filter(), რათა მიიღოთ ახალი სია მხოლოდ დადებითი რიცხვებით
# ბ) გამოიყენეთ map(), რათა ყველა ეს დადებითი რიცხვი გაამრავლოთ 10-ზე  

numbers = [3, -7, 12, 0, -4, 9, 15, -2]

step1 = filter(lambda x:x>0,numbers)

result = list(map(lambda x: x * 10,step1))

print(result)

